#include <unistd.h>

void	ft_print_comb2(void)
{
	char	tab[5];
	int		i;
	int		g;

	i = 0;
	while (i <= 99)
	{
		g = i + 1;
		while (g <= 99)
		{
			tab[0] = '0' + (i / 10);
			tab[1] = '0' + (i % 10);
			tab[2] = ' ';
			tab[3] = '0' + (g / 10);
			tab[4] = '0' + (g % 10);
			write(1, tab, 5);
			if (i != 98 || g != 99)
			{
				write(1, ", ", 2);
			}
			g++;
		}
		i++;
	}
}
